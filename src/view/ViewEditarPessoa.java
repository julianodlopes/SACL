    public void buscarCep() {
        //Faz a busca pelo cep.
        WebServiceCep webServiceCep = WebServiceCep.searchCep(jFormattedTextFieldCEP.getText());
        //A ferramenta de busca ignora qualquer caracter que não seja número.

        //Caso a busca ocorra bem, imprime os resultados.
        if (webServiceCep.wasSuccessful()) {
            jTextFieldLogradouro.setText(webServiceCep.getLogradouroFull());
            jTextFieldCidade.setText(webServiceCep.getCidade());
            jTextFieldBairro.setText(webServiceCep.getBairro());
            jComboBoxEstado.setSelectedItem(webServiceCep.getUf());

            //Caso haja problemas, imprime as excessões.
        } else {
            JOptionPane.showMessageDialog(null, "Erro numero: " + webServiceCep.getResulCode());

            JOptionPane.showMessageDialog(null, "Descrição do erro: " + webServiceCep.getResultText());
        }
    }

