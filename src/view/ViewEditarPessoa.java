    private void jFormattedTextFieldEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jFormattedTextFieldEmailFocusLost
        //Define a expressão regular.
        Pattern p = Pattern.compile("^[\\w-]+(\\.[\\w-]+)*@([\\w-]+\\.)+[a-zA-Z]{2,3}$");

        //Recupera o valor do campo sem os espaços.
        Matcher m = p.matcher(jFormattedTextFieldEmail.getText().trim());

        if (jFormattedTextFieldEmail.getText().equals("")) {
            jLabelVerificaEmail.setVisible(false);
            return;
        }
        //Verifica se está de acordo com o formato.
        if (!m.find()) {
            jFormattedTextFieldEmail.setForeground(Color.red);  //Troca a cor das letras para vermelho.
            okEmail = false;
            jLabelVerificaEmail.setEnabled(true);
            jLabelVerificaEmail.setVisible(true);
        } else {
            jFormattedTextFieldEmail.setForeground(Color.black);  //Troca a cor das letras para preto.
            okEmail = true;
            jLabelVerificaEmail.setEnabled(false);
            jLabelVerificaEmail.setVisible(false);
        }
    }//GEN-LAST:event_jFormattedTextFieldEmailFocusLost

