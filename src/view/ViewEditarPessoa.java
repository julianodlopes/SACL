    //Carrega os dados do banco na janela para evitar a reinserção de todos os dados.
    public void carregarDados() {
        QueryPessoa query = new QueryPessoa();

        ResultSet rs;
        rs = query.retornarCamposPessoa(item);

        try {
            while (rs.next()) {
                jComboBoxRelacionamento.setSelectedItem(rs.getString("relacionamento_ps"));
                jTextFieldNome.setText(rs.getString("nome_ps"));
                jComboBoxSexo.setSelectedItem(rs.getString("sexo_ps"));
                jFormattedTextFieldDataNasc.setValue(rs.getString("data_nasc_ps"));
                jComboBoxEstadoCivil.setSelectedItem(rs.getString("estado_civil_ps"));
                jFormattedTextFieldEmail.setText(rs.getString("email_ps"));
                jFormattedTextFieldResidencial.setValue(rs.getString("tel_residencial_ps"));
                jFormattedTextFieldCelular.setValue(rs.getString("tel_celular_ps"));
                jTextFieldProfissao.setText(rs.getString("profissao_ps"));
                jFormattedTextFieldCEP.setValue(rs.getString("cep_ps"));
                jTextFieldLogradouro.setText(rs.getString("logradouro_ps"));
                jTextFieldNumero.setText(rs.getString("numero_ps"));
                jTextFieldBairro.setText(rs.getString("bairro_ps"));
                jTextFieldCidade.setText(rs.getString("cidade_ps"));
                jComboBoxEstado.setSelectedItem(rs.getString("estado_ps"));
                jTextFieldComplemento.setText(rs.getString("complemento_ps"));
                jTextAreaOBS.setText(rs.getString("obs_ps"));
                jTextAreaOBS2.setText(rs.getString("obs2_ps"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro de SQL. "
                    + "\nDetalhes: " + ex.getMessage());
        }

        if (jFormattedTextFieldDataNasc.getText().equals("  /  /    ")) {
            jFormattedTextFieldDataNasc.setValue("");
        }
    }

