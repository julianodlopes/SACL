    //Carrega os dados do banco dos usuários no jComboBox.
    public void carregarUsuarios() {

        QueryConfiguracoes query = new QueryConfiguracoes();
        ArrayList<String> vetor = new ArrayList<String>();

        ResultSet rs = query.carregarUsuarios();

        try {
            while (rs.next()) {
                vetor.add((rs.getString("nome_usuario")));
            }

            for (int j = 0; j < vetor.size(); j++) {
                jComboBoxUsuarios.addItem(vetor.get(j));
            }

            query.fecharConexao();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro de SQL. "
                    + "\nDetalhes: " + ex.getMessage());
        }
    }

