    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        QueryConfiguracoes query = new QueryConfiguracoes();
        String nomeUsuario = jComboBoxUsuarios.getSelectedItem().toString();

        //Verifica se a exclusão corresponde ao root. Se sim, não permite a exclusão.
        if (nomeUsuario.equals("root")) {
            JOptionPane.showMessageDialog(null, "Desculpe, este usuário é "
                    + "padrão e não pode ser excluido.");
        } else {
            query.removerUsuario(nomeUsuario);
            dispose();
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

